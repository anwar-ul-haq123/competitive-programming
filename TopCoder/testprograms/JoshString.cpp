int isprime(int n)
{
    int l = (int) sqrt(n);
    for(int i = 2; i <= l; i++)
    {
        if(n % i == 0)
            return 0;
    }
    return 1;
}

string JoshString::isJoshString(string s)
{
    int sum = 0;
    for(int i = 0; i < s.size(); i++)
        sum += s[i] - 96;
    if(isprime(sum))
        return "YES";
    else
        return "NO";
}


