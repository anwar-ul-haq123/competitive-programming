class EllysPairs {
public:
	int getDifference(vector <int>);
};

int EllysPairs::getDifference(vector <int> knowledge) {
	sort(knowledge.begin(), knowledge.end());
	int i, j, l;
	l = knowledge.size();
	i = knowledge[0] + knowledge[l-1];
	if(l > 2)
        j = knowledge[l/2-1] + knowledge[l/2];
    else
        j = 0;
    return abs(i - j);
}


