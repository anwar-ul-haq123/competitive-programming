class TopFox {
public:
	int possibleHandles(string, string);
};

int TopFox::possibleHandles(string f, string g) {
	int i, j;
	string a, b;
	map <string, int> mp;
	int mc = 0;
	for(i = 0; i < f.size(); i++)
    {
        a.clear();
        for(int k = 0; k <= i; k++)
            a += f[k];
        for(j = 0; j < g.size(); j++)
        {
            b.clear();
            for(int k = 0; k <= j; k++)
                b += g[k];
            string c = a + b;
            if(mp.find(c) == mp.end())
                mp[c] = mc++;
        }
    }
    return mc;
}


