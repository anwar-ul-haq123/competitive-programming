int trie[MAX][12], fin[MAX], last;

void add(string str)
{
    int i, id, cur = 0;
    for(i = 0; i < SZ(str); i++)
    {
        id = str[i] - '0';
        if(trie[cur][id] == -1)
        {
            trie[cur][id] = ++last;
            clr(trie[last], -1);
        }
        cur = trie[cur][id];
    }
    fin[cur]++;
}

LL get(int cur, int match)
{
    if(cur == -1) return 0;
    if(fin[cur]) return power(10, 9-match);
    LL ret = 0;
    for(int i = 0; i < 10; i++)
        ret += get(trie[cur][i], match+1);
    return ret;
}

