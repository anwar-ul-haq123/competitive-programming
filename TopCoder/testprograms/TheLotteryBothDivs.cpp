class TheLotteryBothDivs {
public:

	double find(vector <string> goodSuffixes) {
	    clr(trie[0], -1), last = 0;
	    clr(fin, 0);
	    for(auto x: goodSuffixes)
        {
            reverse(all(x));
            add(x);
        }
        return 1.0*get(0, 0)/1e9;
	}
};


