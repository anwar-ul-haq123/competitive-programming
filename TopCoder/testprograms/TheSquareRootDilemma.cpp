class TheSquareRootDilemma {
public:
	int countPairs(int, int);
};

int TheSquareRootDilemma::countPairs(int N, int M) {
	int a, b, cnt = 0, i, j = 1;
	for(i = 2; i <= N; )
	{
        for(j = 2; j <= M; )
        {
            cnt++;
            j *= j;
        }
        if(M)
        cnt++;
        i *= i;
	}
	return cnt + M;
}


