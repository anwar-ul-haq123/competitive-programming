class SortInsideNumber {
public:
	int sort(int);
};

int SortInsideNumber::sort(int x) {
    int arr[20];
    vector <int> v;
    v.push_back(5);
    std :: sort(v.begin(), v.end());
	int res = 0, i = 0;
	do
	{
	    arr[i++] = x % 10;
	    x = x / 10;
	}
	while(x);
	for(int p = 0; p < i - 1; p++)
	{
	    for(int q = p; q < i; q++)
	    {
	        if(arr[p] < arr[q])
	        {
	            int temp = arr[p];
	            arr[p] = arr[q];
	            arr[q] = temp;
	        }
	    }
	}
	for(int p = 0; p < i; p++)
        res = res * 10 + arr[p];
	return res;
}


