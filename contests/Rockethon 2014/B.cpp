#define MAX 110
/*************************************************HABIJABI ENDS HERE******************************************************/

LL dp[MAX][MAX][MAX], e[MAX], p[MAX], n, K, have_to_beat;

LL call(int indx, int k, int beat)
{
    if(indx == n)
        return (beat == have_to_beat && k < K)? 0 : infinity;
    LL &ret = dp[indx][k][beat];
    if(ret != -1) return ret;
    ret = call(indx + 1, k + (p[indx] > have_to_beat), beat + 1) + e[indx];
    ret = min(ret, call(indx + 1, k + (p[indx] + 1 >= have_to_beat), beat));
    return ret;
}

