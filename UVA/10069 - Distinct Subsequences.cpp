string cut_leading_zero(string a)
{
    string s;
    int i;
    if(a[0]!='0')   return a;
    for(i = 0; i < SZ(a)-1; i++)  if(a[i]!='0')   break;
    for(; i < SZ(a); i++)  s+=a[i];
    return s;
}

string add(string a,string b)
{
    int carry=0,i;
    string ans;
    if(SZ(a)>SZ(b)) b=string(SZ(a)-SZ(b),'0')+b;
    if(SZ(b)>SZ(a)) a=string(SZ(b)-SZ(a),'0')+a;
    ans.resize(SZ(a));
    for(i = SZ(a)-1; i >= 0; i--)
    {
        int sum=carry+a[i]+b[i]-96;
        ans[i]=(char)(sum%10+'0');
        carry=sum/10;
    }
    if(carry)   ans.insert(0,string(1,carry+'0'));
    ans=cut_leading_zero(ans);
    return ans;
}

string call(int t, int w)
{
    if(t == text_size || w == word_size)
        return w == word_size ? "1" : "0";
    string &ret = dp[t][w];
    if(mark[t][w])
        return ret;
    mark[t][w] = 1;
    ret = call(t + 1, w);
    if(text[t] == word[w]) ret = add(ret, call(t + 1, w + 1));

    return ret;
}

